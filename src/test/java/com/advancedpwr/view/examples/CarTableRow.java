public class CarTableRow
{
	protected String fieldName;
	protected String fieldPrice;
	
	public String getName()
	{
		return fieldName;
	}
	public void setName( String name )
	{
		fieldName = name;
	}
	public String getPrice()
	{
		return fieldPrice;
	}
	public void setPrice( String price )
	{
		fieldPrice = price;
	}
	
	
}
